public void doshow() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/schoolbd", "root", "nh123456");
            pst = con.prepareStatement("Select student_id, firstName,lastName,lavel,student_score from schoolbd.score as sc  INNER JOIN student3 as stab on stab.ID= sc.student_id INNER JOIN course as c on c.ID= sc.course_id");

            rs = pst.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                data = new Object[]{rs.getInt(1), rs.getString(2),rs.getString(3),rs.getString(4),
                    rs.getDouble(5)};
                model.addRow(data);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                pst.close();
            } catch (SQLException ex) {
                Logger.getLogger(UpdateDelete.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
   
